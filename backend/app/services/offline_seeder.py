# ── Main seeder function ─────────────────────────────────────────────────────

async def seed_offline_data(db: AsyncSession) -> None:
    """Idempotent: creates offline KBs + chunks if they don't exist yet.

    Called from main.py lifespan after init_official_sources().
    """
    from app.models.knowledge_base import KnowledgeBase, KBDocument, KBChunk
    from app.models.official_datasource import OfficialDataSource

    # Run column migration for new OfficialDataSource fields
    await _run_datasource_column_migrations(db)

    # Check if offline KBs already exist (idempotent guard)
    existing_names = set(
        (await db.execute(
            select(KnowledgeBase.name).where(KnowledgeBase.name.like("【离线】%"))
        )).scalars().all()
    )

    now = datetime.now(timezone.utc)
    total_chunks_created = 0

    for domain in _OFFLINE_DATA:
        kb_name = domain["kb_name"]
        if kb_name in existing_names:
            logger.debug("[offline_seeder] Skipping existing KB: %s", kb_name)
            continue

        # Create KnowledgeBase
        kb = KnowledgeBase(
            name=kb_name,
            description=f"离线预加载数据：{kb_name.replace('【离线】', '')}",
            scope="corp",
            kb_type=domain["kb_type"],
            owner_id=None,
            embed_model="offline",
            created_at=now,
            updated_at=now,
        )
        db.add(kb)
        await db.flush()  # get kb.id

        doc_count = 0
        chunk_count = 0

        for doc_def in domain["documents"]:
            doc = KBDocument(
                kb_id=kb.id,
                title=doc_def["title"],
                file_type="text",
                file_size=0,
                content_preview=doc_def["chunks"][0][:200] if doc_def["chunks"] else "",
                chunk_count=len(doc_def["chunks"]),
                status="indexed",
                created_at=now,
            )
            db.add(doc)
            await db.flush()  # get doc.id

            for idx, chunk_text in enumerate(doc_def["chunks"]):
                chunk = KBChunk(
                    kb_id=kb.id,
                    doc_id=doc.id,
                    content=chunk_text,
                    chunk_index=idx,
                    embedding_json="",
                    created_at=now,
                )
                db.add(chunk)
                chunk_count += 1

            doc_count += 1

        # Update KB counts
        kb.doc_count = doc_count
        kb.chunk_count = chunk_count

        # Update linked OfficialDataSource entries
        for src_key in domain.get("source_keys", []):
            src = (await db.execute(
                select(OfficialDataSource).where(OfficialDataSource.key == src_key)
            )).scalar_one_or_none()
            if src:
                src.offline_available = True
                src.offline_doc_count = (src.offline_doc_count or 0) + chunk_count

        total_chunks_created += chunk_count
        logger.info("[offline_seeder] Created KB '%s': %d docs, %d chunks", kb_name, doc_count, chunk_count)

    if total_chunks_created > 0:
        await db.commit()
        logger.info("[offline_seeder] Offline seed complete: %d total chunks created", total_chunks_created)
    else:
        logger.info("[offline_seeder] Offline data already seeded, skipping")
