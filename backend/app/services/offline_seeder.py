# ── Column migration helper ──────────────────────────────────────────────────

async def _run_datasource_column_migrations(db: AsyncSession) -> None:
    """Add new columns to official_datasources if they don't exist (SQLite safe)."""
    new_cols = [
        ("requires_api_key", "BOOLEAN DEFAULT 0"),
        ("api_key_name",     "VARCHAR(100) DEFAULT ''"),
        ("api_key_value",    "TEXT DEFAULT ''"),
        ("offline_available","BOOLEAN DEFAULT 0"),
        ("offline_doc_count","INTEGER DEFAULT 0"),
    ]
    try:
        async with db.bind.begin() as conn:
            result = await conn.execute(text("PRAGMA table_info(official_datasources)"))
            existing_cols = {row[1] for row in result.fetchall()}
            for col_name, col_def in new_cols:
                if col_name not in existing_cols:
                    await conn.execute(
                        text(f"ALTER TABLE official_datasources ADD COLUMN {col_name} {col_def}")
                    )
                    logger.info("[offline_seeder] Added column official_datasources.%s", col_name)
    except Exception as exc:
        logger.warning("[offline_seeder] Column migration skipped (non-fatal): %s", exc)


